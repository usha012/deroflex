// header click function
const allHeaderMenus = document.getElementsByClassName("headerCustomMenu")
for(let i = 0; i<allHeaderMenus?.length; i++){
  allHeaderMenus[i]?.addEventListener("click", e => {
    e.stopPropagation();
    const productType = e.target.getAttribute("productType");
    const pageId = e.target.getAttribute("pageId");
    if(productType){
      redirect(productType, pageId)
    }
  })
}

